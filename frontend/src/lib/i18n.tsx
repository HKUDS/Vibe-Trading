type Messages = typeof messages;

const allMessages: Record<Language, Messages> = {
  en: messages as Messages,
  zh: { ...messages, ...messagesZh } as Messages,
  ja: { ...messages, ...messagesJa } as Messages,
};

function isValidLanguage(value: string): value is Language {
  return (LANGUAGES as { code: string }[]).some((lang) => lang.code === value);
}

function detectLanguage(): Language {
  if (typeof window === "undefined") return "en";
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved && isValidLanguage(saved)) {
    return saved;
  }
  const browserLang = navigator.language.toLowerCase();
  if (browserLang.startsWith("zh")) return "zh";
  if (browserLang.startsWith("ja")) return "ja";
  return "en";
}

interface I18nContextValue {
  t: Messages;
  language: Language;
  setLanguage: (lang: Language) => void;
}

const I18nCtx = createContext<I18nContextValue>({ t: allMessages.en, language: "en", setLanguage: () => {} });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(detectLanguage);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem(STORAGE_KEY, lang);
  };

  useEffect(() => {
    const handler = (e: StorageEvent) => {
      if (e.key === STORAGE_KEY && e.newValue && isValidLanguage(e.newValue)) {
        setLanguageState(e.newValue);
      }
    };
    window.addEventListener("storage", handler);
    return () => window.removeEventListener("storage", handler);
  }, []);

  return (
    <I18nCtx.Provider value={{ t: allMessages[language], language, setLanguage }}>
      {children}
    </I18nCtx.Provider>
  );
}

export function useI18n() { return useContext(I18nCtx); }