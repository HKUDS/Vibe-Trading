/* ---------- Types & Context ---------- */
type Messages = Record<keyof typeof enMessages, string>;
type Locale = "zh" | "en";

const messagesMap: Record<Locale, Messages> = { zh: zhMessages, en: enMessages };
const STORAGE_KEY = "vibe-trading-locale";

function getInitialLocale(): Locale {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === "zh" || stored === "en") return stored;
  } catch { /* ignore */ }
  return "zh";
}

interface I18nContextValue {
  t: Messages;
  locale: Locale;
  setLocale: (locale: Locale) => void;
}

const I18nCtx = createContext<I18nContextValue>({
  t: zhMessages,
  locale: "zh",
  setLocale: () => {},
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(getInitialLocale);

  const setLocale = useCallback((next: Locale) => {
    setLocaleState(next);
    try { localStorage.setItem(STORAGE_KEY, next); } catch { /* ignore */ }
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const t = messagesMap[locale];

  return (
    <I18nCtx.Provider value={{ t, locale, setLocale }}>
      {children}
    </I18nCtx.Provider>
  );
}

export function useI18n() { return useContext(I18nCtx); }
